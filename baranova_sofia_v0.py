x = float(input('x = '))
y = float(input('y = '))
c = input('c = ')
if c == '+':
    print(' x + y = ', x + y)
elif c == '-':
    print(' x - y = ', x - y)
elif c == '*':
    print(' x * y = ', x * y)
elif c == '/':
    print(' x / y = ', x / y)
elif c == '//':
    print(' x // y = ', x // y)
elif c == '%':
    print(' x % y = ', x % y)
elif c == '**':
    print(' x ** y = ', x ** y)
